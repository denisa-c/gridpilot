#!/usr/bin/env python3
"""
experiments_v2/scripts/05_extract_macros.py
============================================
Phase 5a — extract LaTeX macros from v2 country_sweep.csv.

Key v2 change vs v1: every Δ macro is emitted with its **explicit
sign character** (``+0.5``, ``-0.5``, or ``0.0``).  The LaTeX table
template MUST NOT add its own ``+`` / ``-`` prefix.  v1's bug was
hardcoded ``+`` in the cell, which produced ``+−0.1`` when the
macro carried a negative value.

Emits ``\\PecsCfeLift{baseline}{Country}``, ``\\PecsCiLift{baseline}{Country}``,
``\\PecsAvoidedT{Country}`` and a few headline scalars
(``\\PecsBaselineUsed``, ``\\StubDataPresent``).

Usage:
  PYTHONPATH=gridpilot/experiments_v2/src python3 \\
      gridpilot/experiments_v2/scripts/05_extract_macros.py \\
      --country-csv gridpilot/experiments_v2/data/country_sweep/country_sweep.csv \\
      --out          gridpilot/experiments_v2/figs/results.tex
"""
from __future__ import annotations

import argparse
import sys
from pathlib import Path

import pandas as pd

ROOT = Path(__file__).resolve().parents[3]

# Headline MW used for the main Table 2.
HEADLINE_MW = 10
# Annualisation: M100 trace is ~56 days (Jan+Feb), scale to 365.
DAYS_IN_TRACE = 56.0
ANNUALISATION = 365.0 / DAYS_IN_TRACE
# Reporting cluster size for "avoided" tonnage column (50 MW).
AVOIDED_REPORT_MW = 50

COUNTRY_ORDER = ["SE", "CH", "FR", "IT", "DE", "PL"]
BASELINES     = ["fcfs", "easy_fcfs", "saf", "replay"]
HEADLINE_LAYER = "fsla_M3"  # the contract's headline mechanism


def _signed(x: float, decimals: int = 1) -> str:
    """Format a signed number with explicit sign character.

    Examples (decimals=1):
        0.5  → "+0.5"
        -0.5 → "-0.5"
        0.0  → "0.0"
    """
    if pd.isna(x):
        return "?"
    if abs(x) < 0.5 * 10 ** (-decimals):
        return f"{0.0:.{decimals}f}"
    sign = "+" if x > 0 else "-"
    return f"{sign}{abs(x):.{decimals}f}"


def _camel(s: str) -> str:
    """Turn 'easy_fcfs' into 'EasyFcfs' for LaTeX-safe macro names."""
    return "".join(part.capitalize() for part in s.split("_"))


def main(argv=None) -> int:
    p = argparse.ArgumentParser()
    p.add_argument("--country-csv", type=Path, required=True)
    p.add_argument("--out",          type=Path, required=True)
    args = p.parse_args(argv)

    if not args.country_csv.exists():
        print(f"ABORT: country_sweep.csv not found at {args.country_csv}",
              file=sys.stderr)
        return 2

    df = pd.read_csv(args.country_csv)
    print(f"[05-extract-macros] read {len(df)} rows from {args.country_csv}")

    # Restrict to the headline MW.
    df_mw = df.query("mw == @HEADLINE_MW")
    if df_mw.empty:
        print(f"WARN: no rows at mw={HEADLINE_MW}; using all rows.")
        df_mw = df

    # Mean over seeds, keyed by (country, layer).
    keys = ["country", "layer"]
    metric_cols = ["energy_kwh", "ci_weighted_mean", "cfe_canonical_pct",
                   "co2_g_facility"]
    delta_cols = [c for c in df_mw.columns
                  if c.startswith("d_cfe_vs_") or c.startswith("d_ci_vs_")]
    means = df_mw.groupby(keys, as_index=False)[metric_cols + delta_cols].mean()

    # Pivot helpers.
    def _get(c: str, layer: str, col: str) -> float:
        rows = means[(means["country"] == c) & (means["layer"] == layer)]
        if rows.empty:
            return float("nan")
        return float(rows.iloc[0][col])

    # ---- Detect stub vs real-data ---------------------------------
    manifest_path = args.country_csv.parent / "RUN_MANIFEST.json"
    stub_present = "true" if not manifest_path.exists() else "false"

    # ---- Emit macros ----------------------------------------------
    out_lines: list[str] = []
    out_lines.append("% =====================================================================")
    out_lines.append("% experiments_v2/figs/results.tex")
    out_lines.append("% Generated by experiments_v2/scripts/05_extract_macros.py")
    out_lines.append(f"% Source: {args.country_csv.relative_to(ROOT) if args.country_csv.is_relative_to(ROOT) else args.country_csv}")
    out_lines.append("% Sign convention: each \\PecsCfeLift / \\PecsCiLift macro")
    out_lines.append("% carries its own sign character.  Do NOT add a + or - prefix in LaTeX.")
    out_lines.append("% =====================================================================")
    out_lines.append("")
    out_lines.append(f"\\newcommand{{\\StubDataPresent}}{{{stub_present}}}")
    out_lines.append(f"\\newcommand{{\\PecsHeadlineMW}}{{{HEADLINE_MW}}}")
    out_lines.append(f"\\newcommand{{\\PecsHeadlineLayer}}{{{HEADLINE_LAYER}}}")
    out_lines.append("")

    # Per-country, per-baseline Δ macros.
    out_lines.append("% Δ CFE (pp) and Δ CI (g/kWh) for the headline layer vs each baseline.")
    for c in COUNTRY_ORDER:
        # Absolute CFE / CI for the country at the headline layer.
        cfe = _get(c, HEADLINE_LAYER, "cfe_canonical_pct")
        ciw = _get(c, HEADLINE_LAYER, "ci_weighted_mean")
        co2 = _get(c, HEADLINE_LAYER, "co2_g_facility")
        out_lines.append(f"\\newcommand{{\\PecsCfe{c}}}{{{cfe:.1f}}}")
        out_lines.append(f"\\newcommand{{\\PecsCi{c}}}{{{ciw:.0f}}}")
        # Annualised facility CO2 in kt/y at the AVOIDED_REPORT_MW scale.
        # Trace is HEADLINE_MW; rescale: factor = AVOIDED_REPORT_MW / HEADLINE_MW.
        scale = AVOIDED_REPORT_MW / HEADLINE_MW
        # Avoided tonnage vs each baseline:
        for base in BASELINES:
            base_co2 = _get(c, base, "co2_g_facility")
            d_co2_g = (base_co2 - co2) * ANNUALISATION * scale
            d_co2_kt = d_co2_g / 1e9
            out_lines.append(
                f"\\newcommand{{\\PecsAvoided{_camel(base)}{c}}}"
                f"{{{_signed(d_co2_kt, 2)}}}"
            )
            # Δ CFE pp
            d_cfe = _get(c, HEADLINE_LAYER, f"d_cfe_vs_{base}_pp")
            out_lines.append(
                f"\\newcommand{{\\PecsCfeLift{_camel(base)}{c}}}"
                f"{{{_signed(d_cfe, 1)}}}"
            )
            # Δ CI g/kWh
            d_ci = _get(c, HEADLINE_LAYER, f"d_ci_vs_{base}_g")
            out_lines.append(
                f"\\newcommand{{\\PecsCiLift{_camel(base)}{c}}}"
                f"{{{_signed(d_ci, 1)}}}"
            )
        out_lines.append("")

    # Cross-country scalars (means over the headline-layer rows).
    headline_rows = means.query("layer == @HEADLINE_LAYER")
    if not headline_rows.empty:
        for base in BASELINES:
            mean_d_cfe = float(headline_rows[f"d_cfe_vs_{base}_pp"].mean())
            mean_d_ci  = float(headline_rows[f"d_ci_vs_{base}_g"].mean())
            out_lines.append(
                f"\\newcommand{{\\PecsMeanCfeLift{_camel(base)}}}"
                f"{{{_signed(mean_d_cfe, 2)}}}"
            )
            out_lines.append(
                f"\\newcommand{{\\PecsMeanCiLift{_camel(base)}}}"
                f"{{{_signed(mean_d_ci, 2)}}}"
            )

    args.out.parent.mkdir(parents=True, exist_ok=True)
    args.out.write_text("\n".join(out_lines) + "\n")
    print(f"[05-extract-macros] wrote {args.out} "
          f"({len(out_lines)} lines)")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
